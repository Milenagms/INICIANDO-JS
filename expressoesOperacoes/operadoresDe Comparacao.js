// Operadores de comparação 

// Irá comparar valores e retornar um Boolean como resposta à comparação

let one = 1 
let two = 2


// ==   igual 
console.log(one == one)

// !=   diferente 
console.log(one != one)

// ===  estritamente igual 
console.log(one === one)

// !==  estritamente diferente 
console.log(one !== one)

// >    Maior 
console.log(one > two)

// >=   Maior igual
console.log(two >= one)

// <=   Menor igual 
console.log(two <= one)